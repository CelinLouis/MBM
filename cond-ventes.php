            Pack Booster 1 (un) mois:
            Pack Booster 1 à 10 annonces
            Pack Booster 11 à 50 annonces
            Pack Booster 51 à 100 annonces
            Pack Booster 101 à 200 annonces

            Pack Booster 3 (trois) mois :
            Pack Booster 1 à 10 annonces
